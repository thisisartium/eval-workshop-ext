# Factory: configure_tracing — create a wired OTelTracingProcessor
# ---------------------------------------------------------------------------

_DEFAULT_ENDPOINT = "http://localhost:4318"


def configure_tracing(
    service_name: str = "agent-platform",
    endpoint: str | None = None,
    capture_content: bool = True,
) -> OTelTracingProcessor:
    """Create an OTelTracingProcessor with OTLP HTTP export.

    Sets up a ``TracerProvider`` with a ``BatchSpanProcessor`` that
    exports spans via OTLP/HTTP (protobuf) to a collector such as
    Cat Cafe. The provider is registered as the global OTel
    ``TracerProvider``.

    Configuration follows OTel conventions: the endpoint can be set
    via the ``endpoint`` argument or the ``OTEL_EXPORTER_OTLP_ENDPOINT``
    environment variable. The service name can be set via argument or
    ``OTEL_SERVICE_NAME``.

    Args:
        service_name: The ``service.name`` resource attribute. Defaults
            to ``"agent-platform"``, overridden by ``OTEL_SERVICE_NAME``.
        endpoint: OTLP collector base URL (e.g. ``http://localhost:4318``).
            Defaults to ``OTEL_EXPORTER_OTLP_ENDPOINT`` or
            ``http://localhost:4318``.
        capture_content: Whether to capture message and tool content
            on spans. Defaults to True for rich trace visibility.

    Returns:
        A configured OTelTracingProcessor ready to pass to
        ``AgentClient.create(processor=...)``.
    """
    from opentelemetry.exporter.otlp.proto.http.trace_exporter import (
        OTLPSpanExporter,
    )
    from opentelemetry.sdk.resources import Resource
    from opentelemetry.sdk.trace import TracerProvider
    from opentelemetry.sdk.trace.export import BatchSpanProcessor

    resolved_name = os.getenv("OTEL_SERVICE_NAME", service_name)
    resource = Resource.create({"service.name": resolved_name})

    provider = TracerProvider(resource=resource)

    resolved_endpoint = endpoint or os.getenv(
        "OTEL_EXPORTER_OTLP_ENDPOINT", _DEFAULT_ENDPOINT
    )
    traces_endpoint = resolved_endpoint.rstrip("/")
    if not traces_endpoint.endswith("/v1/traces"):
        traces_endpoint = f"{traces_endpoint}/v1/traces"

    exporter = OTLPSpanExporter(endpoint=traces_endpoint)
    provider.add_span_processor(BatchSpanProcessor(exporter))
    trace.set_tracer_provider(provider)

    tracer = provider.get_tracer("agent_platform")
    return OTelTracingProcessor(tracer, capture_content=capture_content)
