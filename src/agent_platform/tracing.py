"""OpenTelemetry tracing processor using GenAI semantic conventions.

Translates the platform's internal Span objects into OTel spans with
attributes following the OpenTelemetry GenAI semantic conventions.
The processor accepts an OTel Tracer from the caller, giving full
flexibility over TracerProvider configuration and span export.

Key design decisions:
    - New OTel span/trace IDs are generated by the Tracer (not mapped
      from internal 8-char hex IDs) because OTel requires 128-bit
      trace IDs and 64-bit span IDs.
    - Monotonic timestamps are converted to wall-clock nanoseconds
      using a base offset captured at processor initialization.
    - Parent-child relationships are maintained via a dict mapping
      internal span_id -> OTel span, which is cleaned up on span end.
    - Message content capture is off by default (PII concern) and
      enabled via the capture_content constructor flag.
"""

import json
import os
import time
from collections.abc import Mapping, Sequence
from enum import Enum
from typing import Any

from opentelemetry import trace
from opentelemetry.trace import SpanKind, StatusCode, Tracer

from .observability import (
    AgentSpanData,
    LLMSpanData,
    Span,
    ToolSpanData,
    TracingProcessor,
)

# ---------------------------------------------------------------------------
# Semantic convention format selection
# ---------------------------------------------------------------------------


class SemconvFormat(Enum):
    """Controls which GenAI semantic convention version to emit.

    FLAT (default):
        Uses the pre-v1.37 flat message format with underscore attribute
        names (``gen_ai.input_messages``, ``gen_ai.output_messages``).
        Messages are stored as ``{"role": "...", "content": "..."}``.
        System instructions are included as a system-role message in
        input messages.

    PARTS:
        Uses the v1.37+ parts-based format with dot attribute names
        (``gen_ai.input.messages``, ``gen_ai.output.messages``).
        Messages use ``parts`` arrays with typed entries.
        System instructions are a separate ``gen_ai.system_instructions``
        attribute.
    """

    FLAT = "flat"
    PARTS = "parts"


# ---------------------------------------------------------------------------
# GenAI semantic convention attribute constants
# ---------------------------------------------------------------------------

_OPERATION_NAME = "gen_ai.operation.name"
_PROVIDER_NAME = "gen_ai.provider.name"
_REQUEST_MODEL = "gen_ai.request.model"
_RESPONSE_MODEL = "gen_ai.response.model"
_USAGE_INPUT = "gen_ai.usage.input_tokens"
_USAGE_OUTPUT = "gen_ai.usage.output_tokens"
_AGENT_NAME = "gen_ai.agent.name"
_TOOL_NAME = "gen_ai.tool.name"
_TOOL_CALL_ARGUMENTS = "gen_ai.tool.call.arguments"
_TOOL_CALL_RESULT = "gen_ai.tool.call.result"
_TOOL_DEFINITIONS = "gen_ai.tool.definitions"
_SYSTEM = "gen_ai.system"
_RESPONSE_ID = "gen_ai.response.id"
_REQUEST_TEMPERATURE = "gen_ai.request.temperature"
_REQUEST_TOP_P = "gen_ai.request.top_p"
_ERROR_TYPE = "error.type"

# Attribute names vary by format
_INPUT_MESSAGES_FLAT = "gen_ai.input_messages"
_OUTPUT_MESSAGES_FLAT = "gen_ai.output_messages"
_INPUT_MESSAGES_PARTS = "gen_ai.input.messages"
_OUTPUT_MESSAGES_PARTS = "gen_ai.output.messages"
_SYSTEM_INSTRUCTIONS = "gen_ai.system_instructions"

# OpenLit-style detailed token count attributes (read by Cat Cafe)
_TOKEN_COUNT_PROMPT = "llm.token_count.prompt"
_TOKEN_COUNT_COMPLETION = "llm.token_count.completion"
_TOKEN_COUNT_TOTAL = "llm.token_count.total"
_TOKEN_COUNT_CACHE_READ = "llm.token_count.prompt_details.cache_read"
_TOKEN_COUNT_REASONING = "llm.token_count.completion_details.reasoning"

# Compatibility attributes for trace viewers (e.g. Cat Cafe) that read
# tool I/O from OpenInference-style keys rather than gen_ai.tool.call.*
_INPUT_VALUE = "input.value"
_OUTPUT_VALUE = "output.value"
_INPUT_MIME_TYPE = "input.mime_type"


class OTelTracingProcessor(TracingProcessor):
    """Emits OTel spans with GenAI semantic convention attributes.

    Receives internal Span lifecycle callbacks from the runner and
    creates corresponding OpenTelemetry spans with appropriate
    ``gen_ai.*`` attributes.

    The processor accepts an OTel Tracer, so the caller controls
    TracerProvider configuration (exporters, sampling, etc.).

    Args:
        tracer: An OpenTelemetry Tracer instance.
        capture_content: When True, captures input/output messages on
            LLM spans, and tool arguments/results on tool spans.
            Defaults to False because these may contain PII.
        semconv_format: Controls the message schema and attribute names.
            ``SemconvFormat.FLAT`` (default) uses the pre-v1.37 flat
            message format. ``SemconvFormat.PARTS`` uses the v1.37+
            parts-based format with separate system instructions.
    """

    def __init__(
        self,
        tracer: Tracer,
        capture_content: bool = False,
        semconv_format: SemconvFormat = SemconvFormat.FLAT,
    ) -> None:
        self._tracer = tracer
        self._capture_content = capture_content
        self._semconv_format = semconv_format
        self._otel_spans: dict[str, trace.Span] = {}
        self._monotonic_base = time.monotonic()
        self._wall_clock_base = time.time_ns()

    def on_span_start(self, span: Span) -> None:
        """Create an OTel span and set start-time attributes."""
        name = self._span_name(span)
        kind = self._span_kind(span)
        parent_ctx = self._get_parent_context(span)
        start_ns = self._to_wall_clock_ns(span.started_at)

        otel_span = self._tracer.start_span(
            name=name,
            kind=kind,
            context=parent_ctx,
            start_time=start_ns,
        )

        self._set_start_attributes(otel_span, span)
        self._otel_spans[span.span_id] = otel_span

    def on_span_end(self, span: Span) -> None:
        """Set end-time attributes and close the OTel span."""
        otel_span = self._otel_spans.get(span.span_id)
        if otel_span is None:
            return

        self._set_end_attributes(otel_span, span)

        if span.error is not None:
            otel_span.set_status(StatusCode.ERROR, span.error["message"])
            otel_span.set_attribute(_ERROR_TYPE, span.error["message"])
        else:
            otel_span.set_status(StatusCode.OK)

        end_ns = self._to_wall_clock_ns(span.ended_at) if span.ended_at else None
        otel_span.end(end_time=end_ns)

        del self._otel_spans[span.span_id]

    def shutdown(self) -> None:
        """Clear internal state."""
        self._otel_spans.clear()

    # -- Private helpers ------------------------------------------------------

    @staticmethod
    def _to_parts_input(
        messages: Sequence[Mapping[str, Any]],
    ) -> list[dict[str, Any]]:
        """Convert natural-format input messages to the parts-based schema.

        The runner stores input messages in a simple format
        (``{"role": "user", "content": "..."}`` or tool-result dicts).
        This converts them to the GenAI semantic conventions parts-based
        format for ``gen_ai.input.messages``.

        Conversion rules:
        - ``{"role": R, "content": C}`` →
          ``{"role": R, "parts": [{"type": "text", "content": C}]}``
        - ``{"type": "function_call_output", "call_id": ID, "output": O}`` →
          ``{"role": "tool", "parts": [{"type": "tool_call_response",
          "id": ID, "response": O}]}``
        - Already parts-based messages (have a ``parts`` key) pass through.
        """
        result: list[dict[str, Any]] = []
        for msg in messages:
            if "parts" in msg:
                result.append(dict(msg))
            elif msg.get("type") == "function_call_output":
                result.append(
                    {
                        "role": "tool",
                        "parts": [
                            {
                                "type": "tool_call_response",
                                "id": msg.get("call_id"),
                                "response": msg.get("output", ""),
                            }
                        ],
                    }
                )
            elif "content" in msg:
                result.append(
                    {
                        "role": msg.get("role", "user"),
                        "parts": [{"type": "text", "content": msg["content"]}],
                    }
                )
            else:
                result.append(dict(msg))
        return result

    @staticmethod
    def _to_parts_output(
        messages: Sequence[Mapping[str, Any]],
    ) -> list[dict[str, Any]]:
        """Convert natural-format output messages to the parts-based schema.

        The runner stores output messages in a simple format
        (``{"role": "assistant", "content": "..."}`` for text or
        ``{"role": "assistant", "tool_calls": [...]}`` for tool calls).
        This converts them to the GenAI semantic conventions parts-based
        format for ``gen_ai.output.messages``.

        Conversion rules:
        - ``{"role": "assistant", "content": C}`` →
          ``{"role": "assistant", "parts": [{"type": "text",
          "content": C}], "finish_reason": "stop"}``
        - ``{"role": "assistant", "tool_calls": [...]}`` →
          ``{"role": "assistant", "parts": [{"type": "tool_call",
          "name": N, "arguments": A, "id": ID}, ...],
          "finish_reason": "tool_call"}``
        - Already parts-based messages (have a ``parts`` key) pass through.
        """
        result: list[dict[str, Any]] = []
        for msg in messages:
            if "parts" in msg:
                result.append(dict(msg))
            elif "tool_calls" in msg:
                parts: list[dict[str, Any]] = []
                for tc in msg["tool_calls"]:
                    part: dict[str, Any] = {
                        "type": "tool_call",
                        "name": tc.get("name"),
                        "arguments": tc.get("arguments", {}),
                    }
                    if tc.get("id"):
                        part["id"] = tc["id"]
                    parts.append(part)
                result.append(
                    {
                        "role": "assistant",
                        "parts": parts,
                        "finish_reason": "tool_call",
                    }
                )
            elif "content" in msg:
                result.append(
                    {
                        "role": msg.get("role", "assistant"),
                        "parts": [{"type": "text", "content": msg["content"]}],
                        "finish_reason": "stop",
                    }
                )
            else:
                result.append(dict(msg))
        return result

    @staticmethod
    def _set_flattened_tool_calls_from_parts(
        otel_span: trace.Span, output: list[dict[str, Any]]
    ) -> None:
        """Emit flattened tool call attributes from parts-based format.

        Reads ``{"type": "tool_call", ...}`` entries in ``parts`` arrays.
        """
        for completion_idx, msg in enumerate(output):
            parts = msg.get("parts", [])
            tc_idx = 0
            for part in parts:
                if part.get("type") != "tool_call":
                    continue
                prefix = f"gen_ai.completion.{completion_idx}.tool_calls.{tc_idx}"
                if part.get("name"):
                    otel_span.set_attribute(f"{prefix}.name", part["name"])
                arguments = part.get("arguments")
                if arguments is not None:
                    otel_span.set_attribute(
                        f"{prefix}.arguments",
                        json.dumps(arguments)
                        if isinstance(arguments, dict)
                        else str(arguments),
                    )
                call_id = part.get("id")
                if call_id:
                    otel_span.set_attribute(f"{prefix}.id", call_id)
                tc_idx += 1

    @staticmethod
    def _set_flattened_tool_calls_from_natural(
        otel_span: trace.Span,
        output: Sequence[Mapping[str, Any]],
    ) -> None:
        """Emit flattened tool call attributes from natural format.

        Reads ``tool_calls`` lists directly from natural-format messages.
        """
        completion_idx = 0
        for msg in output:
            tool_calls = msg.get("tool_calls", [])
            for tc_idx, tc in enumerate(tool_calls):
                prefix = f"gen_ai.completion.{completion_idx}.tool_calls.{tc_idx}"
                if tc.get("name"):
                    otel_span.set_attribute(f"{prefix}.name", tc["name"])
                arguments = tc.get("arguments")
                if arguments is not None:
                    otel_span.set_attribute(
                        f"{prefix}.arguments",
                        json.dumps(arguments)
                        if isinstance(arguments, dict)
                        else str(arguments),
                    )
                call_id = tc.get("id")
                if call_id:
                    otel_span.set_attribute(f"{prefix}.id", call_id)
            if tool_calls:
                completion_idx += 1

    def _set_content_parts(self, otel_span: trace.Span, data: LLMSpanData) -> None:
        """Write message content using v1.37+ parts-based format.

        Converts natural-format messages to parts-based schema and writes
        ``gen_ai.input.messages``, ``gen_ai.output.messages``, and
        ``gen_ai.system_instructions`` attributes.  Also emits flattened
        ``gen_ai.prompt.N.*`` for tool result extraction.
        """
        if data.system_instructions is not None:
            otel_span.set_attribute(
                _SYSTEM_INSTRUCTIONS,
                json.dumps([{"type": "text", "content": data.system_instructions}]),
            )
        if data.input is not None:
            parts_input = self._to_parts_input(data.input)
            otel_span.set_attribute(_INPUT_MESSAGES_PARTS, json.dumps(parts_input))
            # Build flat input list with system prompt prepended (same as _set_content_flat)
            flat_input: list[dict[str, Any]] = []
            if data.system_instructions is not None:
                flat_input.append(
                    {"role": "system", "content": data.system_instructions}
                )
            flat_input.extend(data.input)
            self._set_flattened_prompts(otel_span, flat_input)
        if data.output is not None:
            parts_output = self._to_parts_output(data.output)
            otel_span.set_attribute(_OUTPUT_MESSAGES_PARTS, json.dumps(parts_output))
            self._set_flattened_tool_calls_from_parts(otel_span, parts_output)

    def _set_content_flat(self, otel_span: trace.Span, data: LLMSpanData) -> None:
        """Write message content using pre-v1.37 flat format.

        Writes ``gen_ai.input_messages`` and ``gen_ai.output_messages``
        attributes with flat message dicts. System instructions are
        prepended as a system-role message in input messages.

        Also emits flattened ``gen_ai.prompt.N.*`` attributes for input
        messages so that ``extract_retrieval_context`` can find tool
        result content.
        """
        if data.input is not None:
            input_msgs: list[dict[str, Any]] = []
            if data.system_instructions is not None:
                input_msgs.append(
                    {"role": "system", "content": data.system_instructions}
                )
            input_msgs.extend(data.input)
            otel_span.set_attribute(_INPUT_MESSAGES_FLAT, json.dumps(input_msgs))
            self._set_flattened_prompts(otel_span, input_msgs)
        if data.output is not None:
            otel_span.set_attribute(
                _OUTPUT_MESSAGES_FLAT, json.dumps(list(data.output))
            )
            self._set_flattened_tool_calls_from_natural(otel_span, data.output)

    @staticmethod
    def _set_flattened_prompts(
        otel_span: trace.Span,
        input_msgs: list[dict[str, Any]],
    ) -> None:
        """Emit flattened ``gen_ai.prompt.N.*`` attributes for input messages.

        This enables ``extract_retrieval_context`` (cat-experiments) to
        find tool result content by looking for ``gen_ai.prompt.N.role``
        and ``gen_ai.prompt.N.content`` attributes.

        Maps natural-format messages to the flat prompt schema:
        - ``{"role": R, "content": C}`` → ``gen_ai.prompt.N.role = R``,
          ``gen_ai.prompt.N.content = C``
        - ``{"type": "function_call_output", "call_id": ID, "output": O}``
          → ``gen_ai.prompt.N.role = "tool"``,
          ``gen_ai.prompt.N.content = O``,
          ``gen_ai.prompt.N.tool_call_id = ID``
        """
        for idx, msg in enumerate(input_msgs):
            prefix = f"gen_ai.prompt.{idx}"
            if msg.get("type") == "function_call_output":
                otel_span.set_attribute(f"{prefix}.role", "tool")
                output = msg.get("output", "")
                if output:
                    otel_span.set_attribute(f"{prefix}.content", output)
                call_id = msg.get("call_id")
                if call_id:
                    otel_span.set_attribute(f"{prefix}.tool_call_id", call_id)
            elif "role" in msg:
                otel_span.set_attribute(f"{prefix}.role", msg["role"])
                content = msg.get("content", "")
                if content:
                    if not isinstance(content, str):
                        content = json.dumps(content)
                    otel_span.set_attribute(f"{prefix}.content", content)

    def _to_wall_clock_ns(self, monotonic_secs: float) -> int:
        """Convert a monotonic timestamp to wall-clock nanoseconds.

        Uses the offset between monotonic and wall-clock captured at
        processor initialization to translate monotonic seconds into
        the absolute nanosecond timestamps OTel expects.

        Args:
            monotonic_secs: A ``time.monotonic()`` value.

        Returns:
            Wall-clock time in nanoseconds.
        """
        delta_ns = int((monotonic_secs - self._monotonic_base) * 1_000_000_000)
        return self._wall_clock_base + delta_ns

    def _get_parent_context(self, span: Span) -> trace.Context | None:
        """Build an OTel Context referencing the parent span.

        Args:
            span: The internal span whose parent to resolve.

        Returns:
            An OTel Context with the parent span set, or None
            for root spans.
        """
        if span.parent_id is None:
            return None
        parent = self._otel_spans.get(span.parent_id)
        if parent is None:
            return None
        return trace.set_span_in_context(parent)

    def _span_name(self, span: Span) -> str:
        """Determine the OTel span name from SpanData type.

        Uses the entity's own name rather than repeating the operation
        type, since ``gen_ai.operation.name`` already conveys the span
        kind. This matches OpenInference instrumentor conventions where
        agent spans use the agent name, tool spans use the tool name,
        and LLM spans use a generic label like ``"response"``.

        Args:
            span: The internal span.

        Returns:
            A human-readable span name.
        """
        match span.span_data:
            case AgentSpanData(name=name):
                return name
            case LLMSpanData():
                return "response"
            case ToolSpanData(name=name):
                return name
        return "unknown"  # pragma: no cover

    def _span_kind(self, span: Span) -> SpanKind:
        """Determine the OTel SpanKind from SpanData type.

        LLM calls use CLIENT (external API call). Agent and tool
        spans use INTERNAL (in-process execution).

        Args:
            span: The internal span.

        Returns:
            The appropriate OTel SpanKind.
        """
        match span.span_data:
            case LLMSpanData():
                return SpanKind.CLIENT
            case _:
                return SpanKind.INTERNAL

    def _set_start_attributes(self, otel_span: trace.Span, span: Span) -> None:
        """Set attributes known when the span starts.

        Args:
            otel_span: The OTel span to annotate.
            span: The internal span with payload data.
        """
        # Infer provider from model name for LLM spans
        provider = "openai"
        if isinstance(span.span_data, LLMSpanData) and span.span_data.model.startswith("claude"):
            provider = "anthropic"
        otel_span.set_attribute(_PROVIDER_NAME, provider)
        otel_span.set_attribute(_SYSTEM, provider)

        match span.span_data:
            case AgentSpanData(name=name):
                otel_span.set_attribute(_OPERATION_NAME, "invoke_agent")
                otel_span.set_attribute(_AGENT_NAME, name)
            case LLMSpanData(model=model):
                otel_span.set_attribute(_OPERATION_NAME, "chat")
                otel_span.set_attribute(_REQUEST_MODEL, model)
            case ToolSpanData(name=name):
                otel_span.set_attribute(_OPERATION_NAME, "execute_tool")
                otel_span.set_attribute(_TOOL_NAME, name)

    def _set_end_attributes(self, otel_span: trace.Span, span: Span) -> None:
        """Set attributes known only when the span ends.

        Args:
            otel_span: The OTel span to annotate.
            span: The internal span with completed payload data.
        """
        match span.span_data:
            case LLMSpanData() as data:
                otel_span.set_attribute(_RESPONSE_MODEL, data.model)
                if data.response_id:
                    otel_span.set_attribute(_RESPONSE_ID, data.response_id)
                if data.request_params:
                    if "temperature" in data.request_params:
                        otel_span.set_attribute(
                            _REQUEST_TEMPERATURE, data.request_params["temperature"]
                        )
                    if "top_p" in data.request_params:
                        otel_span.set_attribute(
                            _REQUEST_TOP_P, data.request_params["top_p"]
                        )
                if data.usage:
                    input_tokens = data.usage.get("input_tokens", 0)
                    output_tokens = data.usage.get("output_tokens", 0)
                    otel_span.set_attribute(_USAGE_INPUT, input_tokens)
                    otel_span.set_attribute(_USAGE_OUTPUT, output_tokens)
                    # Detailed token counts (OpenLit-style, read by Cat Cafe)
                    otel_span.set_attribute(_TOKEN_COUNT_PROMPT, input_tokens)
                    otel_span.set_attribute(_TOKEN_COUNT_COMPLETION, output_tokens)
                    otel_span.set_attribute(
                        _TOKEN_COUNT_TOTAL, input_tokens + output_tokens
                    )
                    cached = data.usage.get("cached_tokens")
                    if cached is not None:
                        otel_span.set_attribute(_TOKEN_COUNT_CACHE_READ, cached)
                    reasoning = data.usage.get("reasoning_tokens")
                    if reasoning is not None:
                        otel_span.set_attribute(_TOKEN_COUNT_REASONING, reasoning)
                if self._capture_content:
                    if self._semconv_format == SemconvFormat.PARTS:
                        self._set_content_parts(otel_span, data)
                    else:
                        self._set_content_flat(otel_span, data)
                    if data.tools:
                        otel_span.set_attribute(
                            _TOOL_DEFINITIONS, json.dumps(data.tools)
                        )
            case ToolSpanData() as data:
                if self._capture_content:
                    if data.input is not None:
                        otel_span.set_attribute(_TOOL_CALL_ARGUMENTS, data.input)
                        otel_span.set_attribute(_INPUT_VALUE, data.input)
                        otel_span.set_attribute(_INPUT_MIME_TYPE, "application/json")
                    if data.output is not None:
                        otel_span.set_attribute(_TOOL_CALL_RESULT, data.output)
                        otel_span.set_attribute(_OUTPUT_VALUE, data.output)
            case AgentSpanData():
                pass


